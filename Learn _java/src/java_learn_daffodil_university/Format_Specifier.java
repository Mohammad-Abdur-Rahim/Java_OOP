package java_learn_daffodil_university;

public class Format_Specifier {
    public static void main(String[] args) {
        int i=10;
        short s=9;
        char c='R';
        double d= 5915;
        boolean b=true;
        System.out.printf("int vale =%d",i);
        System.out.printf("short =%d",s);
        System.out.printf("Char =%c",c);
        System.out.printf("Double=%f",d);
        System.out.printf("boolean=%b",b);
    }
}
